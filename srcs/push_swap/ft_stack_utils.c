#include "../includes/push_swap.h"

t_stack	*ft_stacklast(t_stack *stack)
{
	if (!stack)
		return (NULL);
	while (stack->next)
		stack = stack->next;
	return (stack);
}

size_t	ft_stacksize(t_stack *stack)
{
	size_t	i;

	i = 0;
	while (stack)
	{
		stack = stack->next;
		i++;
	}
	return (i);
}

int	ft_min(t_stack *a)
{
	int		i;

	i = a->nbr;
	while (a)
	{
		if (a->nbr < i)
			i = a->nbr;
		a = a->next;
	}
	return (i);
}

int	ft_max(t_stack *stack_a)
{
	int		i;

	i = stack_a->nbr;
	while (stack_a)
	{
		if (stack_a->nbr > i)
			i = stack_a->nbr;
		stack_a = stack_a->next;
	}
	return (i);
}
